// Пакет api содержит модели и вспомогательные элементы, связанные с ответами API панели
package api

// APIResponse описывает стандартный формат ответа API панели (успех/сообщение/объект)
type APIResponse struct {
    // Success указывает на успешность выполнения операции на стороне панели
    Success bool   `json:"success"`
    // Msg содержит человекочитаемое сообщение об ошибке/успехе
    Msg     string `json:"msg"`
    // Obj может содержать произвольный полезный объект (например, обновлённые данные)
    Obj     any    `json:"obj"`
}
